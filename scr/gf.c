#include "gf.h"


void
init_gf(size_t m, t_gf	*gf)
{
	gf->m = m;
	gf->mask = (1 << m) - 1;
	gf->f = (1 << m) + (1 << 4) + (1 << 2) + (1 << 1) + 1;
}